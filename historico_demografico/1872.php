  <h2 style="border-bottom:#000000 solid 1px;">Fatos e Fotos </h2>
  <p>&nbsp;</p>
  <div id="quadro-foto-texto">
    <p><img src="img/1872/estacao-luz-1867.jpg" alt="Pr&eacute;dio da antiga esta&ccedil;&atilde;o da Luz" width="200" height="118" /><br />
      Pr&eacute;dio  da antiga esta&ccedil;&atilde;o da Luz,<br />
em uso a partir de 1867</p>
  </div>
  <p>S&atilde;o Paulo em  1872, com pouco mais de 30.000 habitantes, passou a contar com  sua primeira linha de bondes &#9472;&nbsp; Largo do  Carmo/ Esta&ccedil;&atilde;o da Luz &#9472; puxados por tra&ccedil;&atilde;o animal. Apenas em 1900, quando ficou  pronta a Usina El&eacute;trica a Vapor da Rua S&atilde;o Caetano, entraram em funcionamento  os primeiros bondes el&eacute;tricos.</p>
  <p>&nbsp;</p>
  <p>A  Ferrovia Santos-Jundia&iacute; foi inaugurada em 1867, operada pela The S&atilde;o Paulo  Railway Company Ltd., mas as instala&ccedil;&otilde;es da grande esta&ccedil;&atilde;o de passageiros no  bairro da Luz entraram em uso somente em 1901.</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <p>A divis&atilde;o territorial do munic&iacute;pio adotada no Censo de 1872 expressava bem o grande poder de influ&ecirc;ncia da Igreja Cat&oacute;lica de ent&atilde;o: a cidade era divida em 9   &ldquo;parochias&rdquo;: N.S. de Assump&ccedil;&atilde;o da S&eacute;; N.S. da  Concei&ccedil;&atilde;o de Santa Ephigenia; N.S. da Consola&ccedil;&atilde;o de S. Jo&atilde;o Baptista; Senhor Bom  Jesus de Matosinhos do Braz; N.S. da Concei&ccedil;&atilde;o dos Guarulhos; N. S. da  Expecta&ccedil;&atilde;o do &Oacute;; N. S. da Penha de Fran&ccedil;a; N. S. da Concei&ccedil;&atilde;o de S&atilde;o Bernardo;  N. S. do Desterro de Juquery.</p>
  <p>&nbsp;</p>
  <div id="quadro-foto-texto">
    <p><a href="img/1872/rua-libero_grande.jpg"><img src="img/1872/rua-libero_pequeno.jpg" alt="Rua L&iacute;bero Badar&oacute;" width="200" height="150" border="0" title="Clique para ampliar a imagem" /></a></p>
    <p>Fonte: Depto. do Patrim&ocirc;nio Hist&oacute;rico do Munic&iacute;pio de S&atilde;o Paulo. <a href="img/1872/rua-libero_grande.jpg">Ampliar</a> </p>
  </div>
  <h3>Rua L&iacute;bero Badar&oacute;</h3>
  <p>&nbsp;</p>
  <p>Fotografia da Rua L&iacute;bero Badar&oacute;, tomada da esquina da Rua Direita, aproximadamente entre 1870 e 1880. O pr&eacute;dio da esquerda era o antigo solar dos Bar&otilde;es de Itapetininga (mais tarde dos Bar&otilde;es de Tatu&iacute;), demolido em 1889 para a constru&ccedil;&atilde;o do Viaduto do Ch&aacute;.</p>
  <p>&nbsp;</p>
  <p>Os pr&eacute;dios da direita ficavam onde hoje abre-se a Pra&ccedil;a do Patriarca. Primitivamente, a Rua L&iacute;bero Badar&oacute; chamava-se  Rua Nova de S&atilde;o Jos&eacute; e, posteriormente, apenas Rua S&atilde;o Jos&eacute;, por ter sido aberta pelo Capit&atilde;o General Marechal Frei Jos&eacute; Raimundo Chichorro da Gama Lobo, que governou a Capitania de S&atilde;o Paulo de 5 de maio de 1786 a 4 de junho de 1788. Essa rua, at&eacute; 1911, era uma viela estreita, com menos de 8 metros de largura. Seu nome foi mudado para L&iacute;bero Badar&oacute; por uma proposta popular, que foi aceita pela C&acirc;mara Municipal em 19/11/1889, quando Antonio Prado era o presidente da Casa. Determinou-se  seu alargamento em 27/10/1910, realizado na administra&ccedil;&atilde;o do prefeito Raymundo da Silva Duprat (1911 a 1914), e v&aacute;rios melhoramentos foram empreendidos de acordo com  o Plano Bouvard, aprovado em 09/09/1911.</p>
  <p>&nbsp;</p>
  <div id="quadro-foto-texto">
    <p><a href="img/1872/rua-benjamin-constant_grande.jpg"><img src="img/1872/rua-benjamin-constant_pequeno.jpg" alt="Rua Benjamim Constant" title="Clique para ampliar a imagem" width="200" height="150" border="0" /></a></p>
    <p>Fonte: Depto. do Patrim&ocirc;nio Hist&oacute;rico do Munic&iacute;pio de S&atilde;o Paulo. <a href="img/1872/rua-benjamin-constant_grande.jpg">Ampliar</a></p>
  </div>
  <h3>Rua Benjamim Constant</h3>
  <p>&nbsp;</p>
  <p>Autorizado por alvar&aacute; de 19 de  novembro de 1624, foi fundado nesta capital o Convento de S&atilde;o Francisco,  iniciando-se sua edifica&ccedil;&atilde;o em 1639, com inaugura&ccedil;&atilde;o do edif&iacute;cio do Convento em 1828. Em 16 de fevereiro de 1880, ocorreu violento inc&ecirc;ndio no  edif&iacute;cio da Faculdade de Direito, reduzindo a cinzas a maior parte de seu  arquivo e a Capela-mor da Igreja de S&atilde;o Francisco. Em 1935, o edif&iacute;cio foi  demolido, sendo constru&iacute;do no mesmo local o atual pr&eacute;dio.</p>
  <p>&nbsp;</p>
  <p>Fotografia tomada da esquina da Rua Benjamim Constant em  dire&ccedil;&atilde;o ao Convento de S&atilde;o Francisco, em 1862.</p>
  <p>&nbsp;</p>
