  <p>S&atilde;o Paulo em  1872, com pouco mais de 30.000 habitantes, passou a contar com  sua primeira linha de bondes &#9472;&nbsp; Largo do  Carmo/ Esta&ccedil;&atilde;o da Luz &#9472; puxados por tra&ccedil;&atilde;o animal. Apenas em 1900, quando ficou  pronta a Usina El&eacute;trica a Vapor da Rua S&atilde;o Caetano, entraram em funcionamento  os primeiros bondes el&eacute;tricos.</p>
  <p>&nbsp;</p>
  <p>A  Ferrovia Santos-Jundia&iacute; foi inaugurada em 1867, operada pela The S&atilde;o Paulo  Railway Company Ltd., mas as instala&ccedil;&otilde;es da grande esta&ccedil;&atilde;o de passageiros no  bairro da Luz entraram em uso somente em 1901.</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <p>A divis&atilde;o territorial do munic&iacute;pio adotada no Censo de 1872 expressava bem o grande poder de influ&ecirc;ncia da Igreja Cat&oacute;lica de ent&atilde;o: a cidade era divida em 9   &ldquo;parochias&rdquo;: N.S. de Assump&ccedil;&atilde;o da S&eacute;; N.S. da  Concei&ccedil;&atilde;o de Santa Ephigenia; N.S. da Consola&ccedil;&atilde;o de S. Jo&atilde;o Baptista; Senhor Bom  Jesus de Matosinhos do Braz; N.S. da Concei&ccedil;&atilde;o dos Guarulhos; N. S. da  Expecta&ccedil;&atilde;o do &Oacute;; N. S. da Penha de Fran&ccedil;a; N. S. da Concei&ccedil;&atilde;o de S&atilde;o Bernardo;  N. S. do Desterro de Juquery.</p>
