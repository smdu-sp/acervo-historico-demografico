  <h3>Rua Benjamim Constant</h3>
  <p>&nbsp;</p>
  <p>Autorizado por alvar&aacute; de 19 de  novembro de 1624, foi fundado nesta capital o Convento de S&atilde;o Francisco,  iniciando-se sua edifica&ccedil;&atilde;o em 1639, com inaugura&ccedil;&atilde;o do edif&iacute;cio do Convento em 1828. Em 16 de fevereiro de 1880, ocorreu violento inc&ecirc;ndio no  edif&iacute;cio da Faculdade de Direito, reduzindo a cinzas a maior parte de seu  arquivo e a Capela-mor da Igreja de S&atilde;o Francisco. Em 1935, o edif&iacute;cio foi  demolido, sendo constru&iacute;do no mesmo local o atual pr&eacute;dio.</p>
