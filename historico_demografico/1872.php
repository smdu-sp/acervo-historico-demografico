    <p>Fonte: Depto. do Patrim&ocirc;nio Hist&oacute;rico do Munic&iacute;pio de S&atilde;o Paulo. <a href="img/1872/rua-senador-paulo-egidio_grande.jpg">Ampliar</a> </p>
