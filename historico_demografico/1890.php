  <p>&nbsp;</p>
  <p>Desenho ilustrativo da primeira  linha de <em>tramways</em>, enviado ao governo pela The S&atilde;o Paulo Railway, Light and  Power Co. Ltd., solicitando autoriza&ccedil;&atilde;o para implantar o transporte por bondes  el&eacute;tricos na cidade de S&atilde;o Paulo, que se efetivou apenas em 1900.</p>
