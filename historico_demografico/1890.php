  <h2 style="border-bottom:#000000 solid 1px;">Fatos e Fotos </h2>
  <p>&nbsp;</p>
  <p>S&atilde;o   Paulo contava em 1890 com cerca de 65.000 habitantes. O recenseamento mostrou   que a popula&ccedil;&atilde;o paulistana passara a crescer a taxas elevadas. A raz&atilde;o para isso   estava no grande n&uacute;mero de imigrantes estrangeiros que aqui se fixavam. Para o   censo de 1890 foram criados os seguintes &ldquo;districtos&rdquo;: Norte e Sul (que correspondiam &agrave;  Parochia de N. S. da Assump&ccedil;&atilde;o da S&eacute;); Santa Ephigenia e Santana (englobando a parochia de N. S. da  Concei&ccedil;&atilde;o de Santa Ephigenia); Consola&ccedil;&atilde;o (N.S. da Consola&ccedil;&atilde;o de S. Jo&atilde;o  Baptista); Braz (Senhor Bom Jesus de Matosinhos do Braz);&nbsp; N. S. do &Oacute; (N. S. da Expecta&ccedil;&atilde;o do &Oacute;); e Penha  de Fran&ccedil;a (N.S. da Penha de Fran&ccedil;a).  Neste Censo j&aacute; n&atilde;o faziam  parte do Munic&iacute;pio de S&atilde;o Paulo as Parochias de N. S. da Concei&ccedil;&atilde;o de  Guarulhos, N.S. da Concei&ccedil;&atilde;o de S&atilde;o Bernardo e N. S. do Desterro do Juquery  que, mais tarde, passaram a integrar outros munic&iacute;pios ao redor de S&acirc;o Paulo.</p>
