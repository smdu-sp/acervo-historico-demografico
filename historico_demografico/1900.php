  <h3>Pra&ccedil;a da S&eacute; </h3>
  <p>&nbsp;</p>
  <p>Na   passagem do s&eacute;culo XIX para o s&eacute;culo XX a cidade via desaparecerem as marcas da   arquitetura colonial em seu casario. A antiga catedral e a igreja de S&atilde;o Pedro   dos Cl&eacute;rigos logo deram lugar a edifica&ccedil;&otilde;es de estilo ecl&eacute;tico, que dariam um ar   &ldquo;europeizado&rdquo; &agrave; cidade. A atual catedral teve sua constru&ccedil;&atilde;o iniciada em 1913, a   partir de projeto do alem&atilde;o  Maximilian Emil Hehl, professor de Arquitetura da Escola Polit&eacute;cnica. A  inaugura&ccedil;&atilde;o da nave ocorreu em 25 de janeiro de 1954, na comemora&ccedil;&atilde;o do 4&ordm;  Centen&aacute;rio da cidade, mas as obras externas foram conclu&iacute;das apenas em  2003.</p>
