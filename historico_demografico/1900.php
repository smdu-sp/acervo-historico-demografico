  <h3>Parque do Anhagaba&uacute; </h3>
  <p>&nbsp;</p>
  <p>Na   gest&atilde;o do prefeito Raymundo da Silva Duprat (1911 a 1914), o urbanista e   paisagista franc&ecirc;s J. A. Bouvard foi contratado com a finalidade de projetar a   constru&ccedil;&atilde;o de parques e pra&ccedil;as que embelezassem a cidade. Seu trabalho, que   ficou conhecido como Plano Bouvard, resultou na implanta&ccedil;&atilde;o de dois grandes   jardins p&uacute;blicos, um no Vale do Anhangaba&uacute; e outro na V&aacute;rzea do Carmo (este,   posteriormente denominado Parque D. Pedro), bem como na constru&ccedil;&atilde;o do belvedere   do Trianon, na Av. Paulista (no local onde hoje ergue-se o edif&iacute;cio do MASP) e   da pra&ccedil;a Buenos Aires, em Higien&oacute;polis. </p>
