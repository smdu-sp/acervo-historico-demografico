    <p>Mais  uma vista da primeira edifica&ccedil;&atilde;o da esta&ccedil;&atilde;o da Luz; ao fundo, observam-se as  obras da nova esta&ccedil;&atilde;o de passageiros, inaugurada em 1901.</p>
  </div>
  <p>Em  1892 foi inaugurado o Viaduto do Ch&aacute;, constru&iacute;do, em sua primeira vers&atilde;o, em  estrutura met&aacute;lica; foi uma obra de grande import&acirc;ncia para a vida da cidade,  pois permitia maior comodidade ao se atravessar o vale do Anhangaba&uacute;. Seu nome  se deve a uma planta&ccedil;&atilde;o de ch&aacute; da &iacute;ndia que ocupava as margens do rio.</p>
  <p>&nbsp;</p>
  <h3>Curiosidade</h3>
  <p><br />
    At&eacute; 1897, quando o controle do Viaduto do Ch&aacute; passou para o  poder p&uacute;blico, era  cobrado o ped&aacute;gio de 60 r&eacute;is (tr&ecirc;s vint&eacute;ns) por pessoa que o atravessasse. Naquela &eacute;poca, havia uma guarita onde ficavam os funcion&aacute;rios  respons&aacute;veis pela cobran&ccedil;a. Com isso,  ficou conhecido como  &quot;Viaduto dos Tr&ecirc;s Vint&eacute;ns&quot;. </p>
