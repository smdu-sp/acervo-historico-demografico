  <h3>Semana da Arte Moderna em 1922 </h3>
  <h3>&nbsp;</h3>
  <p><strong>O Encontro</strong></p>
  <p> M&aacute;rio de Andrade <em>(sentado)</em>, Anita Malfatti<em> (sentada, ao centro)</em> e  amigos na exposi&ccedil;&atilde;o de Zina Aita <em>(&agrave; esquerda de Anita),</em> em S&atilde;o Paulo, em 1922. As apresenta&ccedil;&otilde;es art&iacute;sticas da Semana de Arte Moderna aconteceram no Theatro Municipal de S&atilde;o Paulo.</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
