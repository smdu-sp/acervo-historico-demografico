    <p>Fonte: Depto. do Patrim&ocirc;nio Hist&oacute;rico do  Munic&iacute;pio de S&atilde;o Paulo. <a href="img/1920/cia-paulista-ferreo_grande.jpg">Ampliar</a> </p>
  </div>
  <h3>Companhia Paulista de Estradas de Ferro </h3>
  <p>&nbsp;</p>
  <p>Fotografia  tomada entre a Rua Flor&ecirc;ncio de Abreu e a  S&atilde;o Bento, em dire&ccedil;&atilde;o ao Viaduto  Santa Ifig&ecirc;nia, por volta de 1920. &Agrave; direita, parte  do Mosteiro de S&atilde;o Bento; ao centro, o in&iacute;cio do Viaduto Santa Ifig&ecirc;nia, inaugurado em 1913. </p>
  <p>&nbsp;</p>
  <p>&Agrave; esquerda, o  pr&eacute;dio onde funcionaram os escrit&oacute;rios da Companhia Paulista de Estradas de Ferro, constru&iacute;do  em 1886 e demolido em 1932.</p>
