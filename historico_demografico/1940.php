  <h3>Revolu&ccedil;&atilde;o  Constitucionalista de 1932</h3>
  <p>&nbsp;</p>
  <p>A cena mais  parece sa&iacute;da de um &aacute;lbum de lembran&ccedil;as da II Guerra Mundial. Mas aconteceu no  centro da capital. &quot;&Eacute;s paulista? Ah! Ent&atilde;o tu me compreendes! Trazes, como  eu, o luto na tua alma e l&acirc;minas de fel no cora&ccedil;&atilde;o&quot;, dizia o poema  distribu&iacute;do nas ruas. Para lutar contra Get&uacute;lio Vargas, mais de 40 000 pessoas  se mobilizaram. Ap&oacute;s tr&ecirc;s meses de conflito e mais de 600 baixas, os rebeldes  acabaram se rendendo. E a chamada Revolu&ccedil;&atilde;o Constitucionalista de 1932 entrou  para a hist&oacute;ria como um dos per&iacute;odos mais conturbados da cidade.<br />
  Fonte: MIS</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <div id="quadro-foto-texto">
    <p align="center"><a href="img/1950/marechal-deodoro_grande.jpg" title="Clique para ampliar esta imagem"><img src="img/1950/marechal-deodoro_pequeno.jpg" alt="Pra&ccedil;a Marechal Deodoro" width="200" height="150" border="0" /></a></p>
    <p>Pra&ccedil;a Marechal Deodoro - <a href="img/1950/marechal-deodoro_grande.jpg">Ampliar</a> </p>
  </div>
  <h3>Pra&ccedil;a Marechal Deodoro</h3>
  <p>&nbsp;</p>
  <p>Este  flagrante de uma S&atilde;o Paulo ainda pacata torna-se mais impressionante por ter  sido registrado na hoje movimentada Pra&ccedil;a Marechal Deodoro, no centro. A  renomada rep&oacute;rter fotogr&aacute;fica alem&atilde; Hildegard Rosenthal fez a imagem em 1940,  como parte de seu estudo sobre tipos encarregados de servi&ccedil;os na capital. Eram  profissionais como o engraxate, o vendedor de jornais e, no caso, o entregador  de leite, com seu ent&atilde;o moderno caminh&atilde;ozinho &ndash; figura que, a partir dos anos  50, desapareceu do cen&aacute;rio urbano. Fonte: Acervo Instituto Moreira  Sales</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <div id="quadro-foto-texto">
    <p align="center"><a href="img/1970/xavier-toledo_grande.jpg" title="Clique para ampliar esta imagem"><img src="img/1970/xavier-toledo_pequeno.jpg" width="200" height="150" border="0" /></a></p>
    <p>Rua Xavier de Toledo, em dire&ccedil;&atilde;o &agrave; Ladeira da Mem&oacute;ria.  <a href="img/1970/xavier-toledo_grande.jpg">Ampliar</a></p>
  </div>
  <h3 align="left">Rua  Xavier de Toledo</h3>
  <p align="left">&nbsp;</p>
  <p>Nos anos 40, o cora&ccedil;&atilde;o da cidade era atravessado por trilhos. Pela Rua  Xavier de Toledo circulava uma dezena de linhas de bonde. Na foto acima,  feita em 1940, &eacute; poss&iacute;vel avistar, ao fundo, a constru&ccedil;&atilde;o do antigo Edif&iacute;cio  Matarazzo, depois conhecido como Banespinha, hoje, sede da Prefeitura de S&atilde;o Paulo. A imagem integra uma s&eacute;rie de dez  postais lan&ccedil;ada pelo Ita&uacute; Cultural, em homenagem ao 450 anos da cidade, comemorados em 2004.</p>
  <p>&nbsp;</p>
  <p>Fonte: Benedito  J. Duarte/Arquivo Hist&oacute;rico Municipal DPH - SMC.</p>
