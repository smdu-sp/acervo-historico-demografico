  <h2 style="border-bottom:#000000 solid 1px;">Fatos e Fotos</h2>
  <p>&nbsp;</p>
  <p>Nos anos 50 S&atilde;o Paulo viu sua   popula&ccedil;&atilde;o passar de pouco mais de 2 milh&otilde;es de habitantes para mais de 3,5   milh&otilde;es. O dinamismo da economia refletia-se ent&atilde;o no aumento da popula&ccedil;&atilde;o: a   cidade crescia impulsionada pelo movimento de expans&atilde;o do setor industrial. As   correntes migrat&oacute;rias respondiam por grande parte do crescimento demogr&aacute;fico e   eram geradas especialmente na regi&atilde;o Nordeste, trazendo mais e mais pessoas,   atra&iacute;das pela possibilidade real de incorpora&ccedil;&atilde;o dos rec&eacute;m chegados ao mercado   de trabalho, seja nas f&aacute;bricas, seja na constru&ccedil;&atilde;o civil. Na segunda metade da   d&eacute;cada a ind&uacute;stria automobil&iacute;stica tornou-se o motor do crescimento econ&ocirc;mico,   com as novas f&aacute;bricas instaladas em munic&iacute;pios vizinhos ao da capital, na regi&atilde;o   que ficou conhecida como ABC (Sto. Andr&eacute;, S. Bernardo e S. Caetano do   Sul).</p>
  <p>&nbsp;</p>
  <p>A regi&atilde;o central de S&atilde;o Paulo fora   ocupada pelos arranha-c&eacute;us, tanto na por&ccedil;&atilde;o de seu n&uacute;cleo hist&oacute;rico, a leste do   vale do Anhangaba&uacute;, conhecida como Centro Velho &ndash; abrigando especialmente sedes   de bancos e institui&ccedil;&otilde;es financeiras &ndash; como na por&ccedil;&atilde;o oeste do vale, em dire&ccedil;&atilde;o   &agrave; pra&ccedil;a da Rep&uacute;blica e conhecida como Centro Novo &ndash; onde se instalaram sobretudo   atividades comerciais e de servi&ccedil;os, em ruas como Bar&atilde;o de Itapetininga, Xavier   de Toledo, Conselheiro Crispiniano, 24 de Maio, Dom Jos&eacute; de   Barros.</p>
  <p>&nbsp;</p>
  <p>As avenidas S&atilde;o Jo&atilde;o e Ipiranga   delimitavam a Cinel&acirc;ndia, principal p&oacute;lo de entretenimento e de vida noturna da   capital, com seus muitos cinemas, restaurantes, bares e cal&ccedil;adas iluminadas por   enormes letreiros de n&eacute;on.</p>
