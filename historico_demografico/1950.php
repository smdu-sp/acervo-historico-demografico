  <h3>Ponte A&eacute;rea une Rio a S&atilde;o Paulo</h3>
  <p>&nbsp;</p>
  <p>Em   1959 era inaugurada a Ponte A&eacute;rea Rio-S&atilde;o Paulo, que passava a oferecer um   servi&ccedil;o ent&atilde;o in&eacute;dito no mundo: embarque imediato, sem necessidade de reservas,   garantido por um <em>pool</em> que reunia   as principais empresas a&eacute;reas da &eacute;poca. Em v&ocirc;os que duravam cerca de 90 minutos,   os modernos Convair aproximavam cariocas e paulistanos, ao final de uma d&eacute;cada   em que a avia&ccedil;&atilde;o civil brasileira conhecera um per&iacute;odo de grande progresso, com   a amplia&ccedil;&atilde;o do n&uacute;mero de rotas ligando cidades de todo o Brasil e com a   moderniza&ccedil;&atilde;o da frota, que incorporou avi&otilde;es projetados no p&oacute;s-guerra   especialmente para o transporte de passageiros. Na foto, um Convair 340 da   Cruzeiro do Sul em Congonhas, pronto para decolar rumo ao Santos Dumont, no Rio. </p>
