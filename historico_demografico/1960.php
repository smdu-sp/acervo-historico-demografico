  <p>&nbsp;</p>
  <p>A capital se  alvoro&ccedil;ou no dia de sua inaugura&ccedil;&atilde;o, em 28 de novembro de 1966. Era o primeiro Shopping a ser constru&iacute;do na cidade. Naquela &eacute;poca,  foi dif&iacute;cil convencer os ent&atilde;o lojistas instalados nas ruas a abrirem filiais em um  espa&ccedil;o fechado. Por isso, a maioria dos comerciantes entrou numa disputa para  ficar o mais perto poss&iacute;vel da entrada. Em pouco tempo, por&eacute;m, o Shopping  Iguatemi (na foto, na d&eacute;cada de 70, antes da reforma do pr&eacute;dio) transformou-se  em sucesso de p&uacute;blico.</p>
