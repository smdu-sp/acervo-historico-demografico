  <h3>Mans&atilde;o Matarazzo</h3>
  <p>&nbsp;</p>
  <p>O casar&atilde;o, com  16  salas e  19 quartos, abrigou a fam&iacute;lia de industriais italianos mais poderosa da cidade, na primeira metade do s&eacute;culo 20. A mans&atilde;o, uma das &uacute;ltimas que restava na Avenida Paulista, foi demolida em 1996. Atualmente, um estacionamento funciona no local.<br />
    <br />
    Fonte: <a href="http://vejinha.abril.com.br/especiais/antesedepois.html" target="_blank">Portal Veja S&atilde;o  Paulo</a></p>
