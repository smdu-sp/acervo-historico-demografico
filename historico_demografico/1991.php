  <h3>Masp</h3>
  <p>&nbsp;</p>
  <p>Museu de Arte de S&atilde;o Paulo  Assis Chateaubriand ou, simplesmente, Masp, 
    inaugurado em 02/10/1947.
    A atual sede na Avenida  Paulista, projetada pela arquiteta Lia Bo Bardi, foi inaugurada em 07/11/1968  por Assis Chateaubriand, que contou com a presen&ccedil;a da Rainha Elizabeth II, da  Inglaterra.</p>
  <p><br />
    De mar&ccedil;o de 1996 a setembro de 2001,  o Museu passou por uma revitaliza&ccedil;&atilde;o interna e externa, quando ganhou o atual  visual com a pintura dos pilares em vermelho.</p>
