  <h3>Masp</h3>
  <p>&nbsp;</p>
  <p>Museu de Arte de S&atilde;o Paulo  Assis Chateaubriand ou, simplesmente, Masp, 
    inaugurado em 02/10/1947.
    A atual sede na Avenida  Paulista, projetada pela arquiteta Lia Bo Bardi, foi inaugurada em 07/11/1968  por Assis Chateaubriand, que contou com a presen&ccedil;a da Rainha Elizabeth II, da  Inglaterra.</p>
  <p><br />
    De mar&ccedil;o de 1996 a setembro de 2001,  o Museu passou por uma revitaliza&ccedil;&atilde;o interna e externa, quando ganhou o atual  visual com a pintura dos pilares em vermelho.</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <div id="quadro-foto-texto">
    <p align="center"><a href="img/1991/espaco-unibanco_grande.jpg" title="Clique para ampliar esta imagem"><img src="img/1991/espaco-unibanco_pequeno.jpg" width="200" height="150" border="0" /></a></p>
    <p>1995 - Alexandre Tokinaka. <a href="img/1991/espaco-unibanco_grande.jpg">Ampliar</a></p>
  </div>
  <h3 align="left">Espa&ccedil;o Unibanco de Cinema</h3>
  <p>&nbsp;</p>
  <p>Um dos lugares mais <em>cults</em> da cidade, ponto de encontro daqueles que gostam de ver um &quot;filme-cabe&ccedil;a&quot;, o Espa&ccedil;o Unibanco de Cinema, na Rua Augusta, j&aacute; se chamou Espa&ccedil;o Banco Nacional de Cinema.</p>
  <p>&nbsp;</p>
  <p>Fonte: <a href="http://vejinha.abril.com.br/especiais/antesedepois.html" target="_blank">Portal Veja S&atilde;o  Paulo</a></p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <div id="quadro-foto-texto">
    <p align="center"><a href="img/1991/pinacoteca_grande.jpg" title="Clique para ampliar esta imagem"><img src="img/1991/pinacoteca_pequeno.jpg" width="200" height="150" border="0" /></a></p>
    <p>1991 - Miguel Boyayan. <a href="img/1991/pinacoteca_grande.jpg">Ampliar</a></p>
  </div>
  <h3>Pinacoteca do Estado</h3>
  <h3>&nbsp;</h3>
  <p>Com paredes de tijolos aparentes e janelas amplas que integram o interior do edif&iacute;cio &agrave; rua, o pr&eacute;dio, projetado por Ramos de Azevedo em 1897, passou por uma reforma durante o governo de M&aacute;rio Covas. Hoje &eacute; um dos principais museus da cidade.<br />
  </p>
  <p>Fonte: <a href="http://vejinha.abril.com.br/especiais/antesedepois.html" target="_blank">Portal Veja S&atilde;o  Paulo</a></p>
  <h3 align="left">&nbsp;</h3>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <div id="quadro-foto-texto">
    <p align="center"><a href="img/1991/sambodromo_grande.jpg" title="Clique para ampliar esta imagem"><img src="img/1991/sambodromo_pequeno.jpg" width="200" height="150" border="0" /></a></p>
    <p>1991 - Miguel Boyaya. <a href="img/1991/sambodromo_grande.jpg">Ampliar</a></p>
  </div>
  <h3>Samb&oacute;dromo do Anhembi</h3>
  <p>&nbsp;</p>
  <p>Velha reivindica&ccedil;&atilde;o dos sambistas da cidade, o Samb&oacute;dromo do Anhembi foi inaugurado em 1991. Antes, os desfiles das escolas de samba eram realizados na Avenida Tiradentes. Com a nova passarela do samba, o carnaval paulistano passou a ser comparado ao do Rio de Janeiro, pela excel&ecirc;ncia dos desfiles. <br />
  </p>
  <p>Fonte: <a href="http://vejinha.abril.com.br/especiais/antesedepois.html" target="_blank">Portal Veja S&atilde;o  Paulo</a></p>
  <p align="left">&nbsp;</p>
