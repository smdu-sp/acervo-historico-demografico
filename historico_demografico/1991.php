  <p>&nbsp;</p>
  <div id="quadro-foto-texto">
    <p align="center"><a href="img/2000/masp_grande.jpg" title="Clique para ampliar esta imagem"><img src="img/2000/masp_pequeno.jpg" alt="Masp" width="200" height="150" border="0" /></a></p>
