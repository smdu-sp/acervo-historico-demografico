        <td width="71%" valign="top"><h2>Hist&oacute;rico Demogr&aacute;fico do Munic&iacute;pio de S&atilde;o Paulo</h2>
          <p>&nbsp;</p>
          <img src="img/box_produto.jpg" width="113" height="240" style="float:right; margin:0px 0px 0px 20px;" />

          <p>Pelas  p&aacute;ginas deste Hist&oacute;rico Demogr&aacute;fico, editado pela Secretaria Municipal de  Desenvolvimento Urbano, &eacute; poss&iacute;vel acompanhar mais de 100 anos de evolu&ccedil;&atilde;o da cidade de  S&atilde;o Paulo, por meio de informa&ccedil;&otilde;es censit&aacute;rias colhidas desde 1872, data do 1&ordm;  censo demogr&aacute;fico feito no Brasil, at&eacute; o mais recente, realizado em 2010.</p>
          <p>&nbsp;</p>
          <p>S&atilde;o  oferecidas tr&ecirc;s op&ccedil;&otilde;es de consulta ao <em>site</em>. Clicando-se em <strong><a href="tabelas.php">tabelas</a></strong>, abre-se o acesso aos dados sobre a popula&ccedil;&atilde;o paulistana,  coletados em cada um dos 12 censos j&aacute; produzidos. Taxas de crescimento,  composi&ccedil;&atilde;o et&aacute;ria e divis&atilde;o por g&ecirc;nero, al&eacute;m de uma vis&atilde;o da popula&ccedil;&atilde;o do  munic&iacute;pio de S&atilde;o Paulo inserida nos contextos metropolitanos, estadual e  nacional, s&atilde;o algumas das possibilidades de consulta oferecidas. Informa&ccedil;&otilde;es  referentes &agrave;s diversas por&ccedil;&otilde;es do munic&iacute;pio est&atilde;o presentes nos dados sobre seus  96 distritos, incluindo o recurso &agrave;s retroproje&ccedil;&otilde;es, que permitem comparar  dados distritais de 1991, 2000 e 2010 com aqueles coletados entre 1950 e 1980, antes,  portanto, da cria&ccedil;&atilde;o da atual divis&atilde;o territorial do munic&iacute;pio.</p>
          <p>&nbsp;</p>
        <p>Essas  informa&ccedil;&otilde;es est&atilde;o acompanhadas de rico material iconogr&aacute;fico, compondo precioso  acervo de imagens que captam as fei&ccedil;&otilde;es da cidade ao longo do tempo e registram as  transforma&ccedil;&otilde;es que a conduziram do s&eacute;culo XIX ao XXI. Para acesso &agrave;s fotos e mapas, que v&ecirc;m acompanhados de textos explicativos, clique em <strong><a href="1872.php">Fotos e Mapas</a></strong> e navegue pelas datas dos Censos. Para ver um breve filme em <em>flash</em>, com fundo musical &quot;S&atilde;o Paulo, S&atilde;o Paulo&quot; do Grupo Prem&ecirc;, clique no bot&atilde;o <strong><a href="animacao.php">Anima&ccedil;&atilde;o</a></strong>.</p></td>
