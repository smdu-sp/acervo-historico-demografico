        <h3>Apresentação</h3>
        <p><em>Olhar São Paulo – Contrastes Urbanos</em> é uma publicação da Sempla que traça um perfil do município,
            com
            foco no tema das desigualdades socioterritoriais. Mapas temáticos e gráficos, acompanhados de textos curtos
            e
            informativos, mas que buscam manter um enfoque analítico, oferecem ao leitor um material rico e variado,
            que, por
            sua estrutura enxuta, permite agilidade de consulta e apresenta uma síntese sobre a cidade.</p>
        <p>O volume está organizado de modo a situar o município no contexto metropolitano e a compor três quadros
            analíticos – demográfico, econômico e social –, este último focado especialmente nos grupos populacionais
            mais
            expostos à situação de pobreza ou de vulnerabilidade social. As relações que se estabelecem entre o
            território,
            sua infra-estrutura e os temas da cidadania – violência e distribuição espacial das oportunidades econômicas
            –
            também são objeto deste trabalho, já que o modo pelo qual o espaço construído está estruturado em São Paulo
            tende
            a manter ou reforçar as situações de exclusão e de vulnerabilidade sociais vividas por expressiva parcela de
            seus
            moradores.</p>
        <p>A gestão de um espaço urbano marcadamente fragmentado, que produz e acentua desigualdades, coloca-se hoje
            como o
            grande desafio político e administrativo ao poder público municipal. Otimizar recursos, sempre escassos,
            quando se
            tem em vista a escala urbana a que se destinam e sobre a qual se deve atuar, implica a formulação de
            políticas
            públicas ancoradas em um sólido conhecimento da realidade e fortemente direcionadas a objetivos claros e
            específicos.</p>
        <p>Nesse sentido, acreditamos que esta publicação efetivamente contribui para o conhecimento das múltiplas
            dimensões
            do território, passo fundamental para embasar a ação planejada do poder público, tendo em vista corrigir ou
            amenizar a segregação territorial, bem como ampliar as condições de cidadania para o conjunto da população.
        </p>
        <p><strong>Manuelito Pereira Magalhães Júnior</strong><br />
            Secretário Municipal de Planejamento</p>
